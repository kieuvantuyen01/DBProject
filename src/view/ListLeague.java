    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        int row = leagueTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListLeague.this, 
                    "Vui long chon League truoc", "Loi", JOptionPane.ERROR_MESSAGE);
        } else {
            int confirm = JOptionPane.showConfirmDialog(ListLeague.this, 
                    "Ban chac chan muon xoa khong?");
            if (confirm == JOptionPane.YES_OPTION) {
                String leagueId = String.valueOf(leagueTable.getValueAt(row, 0));
                
                leagueService.deleteLeague(leagueId);
                
                defaultTableModel.setRowCount(0);
                setTableData(leagueService.getAllLeagues());
            }
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

    private void editButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editButtonActionPerformed
        // TODO add your handling code here:
        int row = leagueTable.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(ListLeague.this, 
                    "Vui long chon League truoc", "Loi", JOptionPane.ERROR_MESSAGE);
        } else {

            String leagueId = String.valueOf(leagueTable.getValueAt(row, 0));

            new EditLeague(leagueId).setVisible(true);
            this.dispose();
        }
    }//GEN-LAST:event_editButtonActionPerformed

